function buildDefaultReminders(reminders = {}) {
  const dayOfReminders = buildDayOfReminderSlots(reminders);
  return {
    dayBeforeEnabled:
      typeof reminders.dayBeforeEnabled === "boolean" ? reminders.dayBeforeEnabled : DEFAULT_REMINDERS.dayBeforeEnabled,
    dayBeforeTime: isValidTimeValue(reminders.dayBeforeTime) ? reminders.dayBeforeTime : DEFAULT_REMINDERS.dayBeforeTime,
    dayOfReminders
  };
}

function isValidTimeValue(value) {
  return typeof value === "string" && /^\d{2}:\d{2}$/.test(value);
}

function buildDayOfReminderSlots(reminders = {}) {
  const defaultSlots = DEFAULT_DAY_OF_REMINDERS.map((slot) => ({ ...slot }));

  if (Array.isArray(reminders.dayOfReminders)) {
    return defaultSlots.map((slot, index) => {
      const candidate = reminders.dayOfReminders[index] || {};
      return {
        enabled: typeof candidate.enabled === "boolean" ? candidate.enabled : slot.enabled,
        time: isValidTimeValue(candidate.time) ? candidate.time : slot.time
      };
    });
  }

  const legacyEnabled = typeof reminders.dayOfEnabled === "boolean" ? reminders.dayOfEnabled : defaultSlots[0].enabled;
  const legacyTime = isValidTimeValue(reminders.dayOfTime) ? reminders.dayOfTime : defaultSlots[0].time;
  defaultSlots[0] = {
    enabled: legacyEnabled,
    time: legacyTime
  };
  return defaultSlots;
}

function getStreetOrientation(points) {
  const [firstLat, firstLon] = points[0];
  const [lastLat, lastLon] = points[points.length - 1];
  const latDiff = Math.abs(lastLat - firstLat);
  const lonDiff = Math.abs(lastLon - firstLon);
  return lonDiff >= latDiff ? "east-west" : "north-south";
}

function normalizeToken(text) {
  return String(text || "").toUpperCase().replace(/\s+/g, " ").trim();
}

function formatDate(dateString) {
  if (!dateString) {
    return "No scheduled date returned";
  }

  const [month, day, year] = dateString.split("/").map(Number);
  const date = new Date(year, month - 1, day, 9, 0, 0);
  return date.toLocaleDateString("en-US", {
    weekday: "short",
    month: "short",
    day: "numeric",
    year: "numeric"
  });
}

function offsetPoint([lat, lon], orientation, sideKey) {
  const latOffset = 0.000035;
  const lonOffset = 0.000045;

  if (orientation === "east-west") {
    return [lat + (sideKey === "north" ? latOffset : -latOffset), lon];
  }

  return [lat, lon + (sideKey === "east" ? lonOffset : -lonOffset)];
}

function serializeSegment(segment) {
  return {
    id: segment.id,
    street: segment.street,
    sideKey: segment.sideKey,
    sideLabel: segment.sideLabel,
    color: segment.color,
    highway: segment.highway,
    geometry: Array.isArray(segment.geometry) ? segment.geometry.map((point) => [...point]) : [],
    schedule: segment.schedule ? { ...segment.schedule, allDates: Array.isArray(segment.schedule.allDates) ? [...segment.schedule.allDates] : [] } : null
  };
}

function getSelectedSegments() {
  return state.currentSelectionIds.map(getSegmentById).filter(Boolean);
}

function buildSegmentSideDefinitions(orientation) {
  return orientation === "east-west"
    ? [
        { sideKey: "north", color: colors.north },
        { sideKey: "south", color: colors.south }
      ]
    : [
        { sideKey: "east", color: colors.east },
        { sideKey: "west", color: colors.west }
      ];
}

function normalizeWayRecord(way) {
  return {
    id: way.id,
    name: way.name,
    highway: way.highway,
    geometry: way.geometry,
    orientation: getStreetOrientation(way.geometry)
  };
}

function hydrateSavedSet(set, validIds) {
  const fallbackSegments = Array.isArray(set.segmentIds)
    ? set.segmentIds
        .map(getSegmentById)
        .filter(Boolean)
        .map(serializeSegment)
    : [];
  const segments = Array.isArray(set.segments) && set.segments.length ? set.segments : fallbackSegments;
  const nextSegmentIds = Array.isArray(set.segmentIds)
    ? set.segmentIds.filter((id) => validIds.has(id))
    : segments.map((segment) => segment.id).filter((id) => validIds.has(id));

  if (!segments.length) {
    return null;
  }

  return {
    ...set,
    segmentIds: nextSegmentIds,
    segments,
    sourceLabel: set.sourceLabel || "Saved curb set",
    lookupAddress: set.lookupAddress || "",
    reminders: buildDefaultReminders(set.reminders)
  };
}

function getSegmentsForSavedSet(set) {
  if (Array.isArray(set.segments) && set.segments.length) {
    return set.segments;
  }

  return (Array.isArray(set.segmentIds) ? set.segmentIds : []).map(getSegmentById).filter(Boolean).map(serializeSegment);
}

function loadSavedState() {
  const validIds = new Set(state.curbSegments.map((segment) => segment.id));
  state.currentSelectionIds = loadJson(CURRENT_SELECTION_KEY, []).filter((id) => validIds.has(id));
  state.savedSets = loadJson(SAVED_SETS_KEY, []).map((set) => hydrateSavedSet(set, validIds)).filter(Boolean);
}

